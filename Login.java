package com.model.shopping;

public class Login {

	private int loginid;
	private int loginstatus;
	private String username;
	private String password;
	/**
	 * 
	 * @return loginid 
	 */
	public int getLoginid() {
		return loginid;
	}
	/**
	 * 
	 * @param loginid
	 *   Return void
	 * Set values into class private variable loginid
	 
	 */
	public void setLoginid(int loginid) {
		this.loginid = loginid;
	}
	/**
	 * 
	 * @return loginstatus
	 */
	public int getLoginstatus() {
		return loginstatus;
	}
	/**
	 * 
	 * @param loginstatus
	 *  Return void
	 * Set values into class private variable loginstatus
	 */
	public void setLoginstatus(int loginstatus) {
		this.loginstatus = loginstatus;
	}
	/**
	 * 
	 * @return username
	 */
	public String getUsername() {
		return username;
	}
	/**
	 * 
	 * @param username
	 *  Return void
	 * Set values into class private variable username
	 */
	public void setUsername(String username) {
		this.username = username;
	}
	/**
	 * 
	 * @return password
	 */
	
	public String getPassword() {
		return password;
	}
	/**
	 * 
	 * @param password
	 * Return void
	 * Set values into class private variable password
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	
}
